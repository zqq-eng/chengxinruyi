  // ===== 点击条目：打开自定义大弹窗（不跳转页面） =====
  openDetail(e) {
    if (this._tapLock) return;
    this._tapLock = true;
    setTimeout(() => (this._tapLock = false), 260);

    // 兼容多种 data 写法
    let cid = e.currentTarget?.dataset?.cid || "";
    let aid = e.currentTarget?.dataset?.aid || "";
    const id = e.currentTarget?.dataset?.id || "";

    // 兜底：如果没传 cid/aid，尝试从 id 解析：A_A1
    if ((!cid || !aid) && id && String(id).includes("_")) {
      const parts = String(id).split("_");
      if (!cid) cid = parts[0] || "";
      if (!aid) aid = parts[1] || "";
    }

    if (!cid || !aid) {
      wx.showToast({ title: "参数缺失", icon: "none" });
      return;
    }

    this._openArticleModal(cid, aid);
  },

  // ✅ 统一打开文章弹窗：弹窗展示“正文 content”，不再展示封面 summary
  _openArticleModal(cid, aid) {
    const db =
      (app.globalData && app.globalData.fxKnowledgeIndexDB)
        ? app.globalData.fxKnowledgeIndexDB
        : this._getKnowledgeIndexDB();

    const cat = db[cid];
    const indexArticle = cat?.articles?.find(x => String(x.aid) === String(aid));

    if (!cat || !indexArticle) {
      wx.showToast({ title: "文章索引不存在", icon: "none" });
      return;
    }

    const ARTICLE_MAP = this._getArticleMap();
    const body = ARTICLE_MAP[String(aid)] || null;

    const modalTitle = String((body && body.title) ? body.title : (indexArticle.title || "未命名文章"));
    const categoryTitle = String(cat.title || cid);

    // 正文只来自 content（避免和封面 summary 混淆）
    const fullText = (body && body.content) ? String(body.content) : "";
    const cleaned = this._cleanArticleText(fullText);

    let contentToShow = "";
    if (!cleaned) {
      contentToShow =
        `【分类】${categoryTitle}\n\n` +
        `⚠️ 该文章正文未录入。\n\n` +
        `请把正文粘贴到：\n` +
        `pages/fitnessKnowledge/fitnessKnowledge.js\n` +
        `→ _getArticleMap() → ARTICLE_MAP.${aid}.content\n`;
    } else {
      contentToShow = `【分类】${categoryTitle}\n\n${cleaned}`;
    }

    this.setData({
      modalVisible: true,
      modalTitle,
      modalCategory: categoryTitle,
      modalContent: contentToShow,
      modalCid: cid,
      modalAid: aid
    });
  },

  // 上一篇 / 下一篇（基于当前 viewList 顺序）
  prevArticle() {
    const { modalCid, modalAid, viewList } = this.data;
    const curId = `${modalCid}_${modalAid}`;
    const idx = (viewList || []).findIndex(x => String(x.id) === String(curId));
    const prevItem = (idx > 0) ? viewList[idx - 1] : null;

    if (!prevItem) {
      wx.showToast({ title: "已是第一篇", icon: "none" });
      return;
    }
    this._openArticleModal(prevItem.cid, prevItem.aid);
  },

  nextArticle() {
    const { modalCid, modalAid, viewList } = this.data;
    const curId = `${modalCid}_${modalAid}`;
    const idx = (viewList || []).findIndex(x => String(x.id) === String(curId));
    const nextItem = (idx >= 0 && idx + 1 < viewList.length) ? viewList[idx + 1] : null;

    if (!nextItem) {
      wx.showToast({ title: "已是最后一篇", icon: "none" });
      return;
    }
    this._openArticleModal(nextItem.cid, nextItem.aid);
  },

  // ===== 文本清理：解决“一两字一行”、空行过多、末尾空格 =====
  _cleanArticleText(text) {
    if (!text) return "";
    let s = String(text);

    s = s.replace(/\r\n/g, "\n").replace(/\r/g, "\n");
    s = s.split("\n").map(line => line.replace(/[ \t]+$/g, "")).join("\n");
    s = s.replace(/\n{3,}/g, "\n\n");

    const lines = s.split("\n");
    const merged = [];
    for (let i = 0; i < lines.length; i++) {
      const line = lines[i];
      const trimmed = line.trim();

      if (!trimmed) {
        if (merged.length && merged[merged.length - 1] === "") continue;
        merged.push("");
        continue;
      }

      // 小标题识别：保持独立一行
      const isHeading = /^【.+】$/.test(trimmed);
      if (isHeading) {
        if (merged.length && merged[merged.length - 1] !== "") merged.push("");
        merged.push(trimmed);
        continue;
      }

      // 很短的一行（<=3）倾向合并到上一行，避免“碎行”
      if (trimmed.length <= 3 && merged.length && merged[merged.length - 1] !== "") {
        merged[merged.length - 1] = `${merged[merged.length - 1]}${trimmed}`;
        continue;
      }

      merged.push(line);
    }

    return merged.join("\n").replace(/\n{3,}/g, "\n\n").trim();
  },

  // ===== 生成展示列表（文章级：用于搜索/默认展示） =====
  _rebuildViewList() {
    const db =
      (app.globalData && app.globalData.fxKnowledgeIndexDB)
        ? app.globalData.fxKnowledgeIndexDB
        : this._getKnowledgeIndexDB();

    const kw = (this.data.keyword || "").trim().toLowerCase();
    const acLabel = String(this.data.activeCategory || "ALL").trim();

    // ✅ 按钮文字 -> DB 分类 key（A~J）
    const mapLabelToKey = (label) => {
      const s = String(label || "").trim();
      if (!s || s === "ALL" || s === "全部") return "ALL";

      const mapping = {
        "减肥与体脂": "A",
        "运动生理": "B",
        "时长与计划": "C",
        "器材与动作": "D",
        "减脂训练体系": "E",
        "女性运动医学": "F",
        "男性运动医学": "G",
        "损伤与康复": "H",
        "睡眠与恢复": "I",
        "饮食与营养": "J"
      };

      const key = mapping[s];
      if (key && db[key]) return key;
      if (db[s]) return s;
      return "ALL";
    };

    const activeKey = mapLabelToKey(acLabel);

    // 聚合所有文章（索引库）
    const allArticles = [];
    Object.keys(db).forEach((k) => {
      const cat = db[k];
      if (!cat || !cat.articles) return;
      cat.articles.forEach((a) => {
        allArticles.push({
          id: `${k}_${a.aid}`,
          cid: k,
          aid: a.aid,
          title: a.title,
          summary: a.summary || "",
          category: cat.title,
          tags: a.tags || [],
          desc: a.desc || ""
        });
      });
    });

    // 分类过滤
    let list = allArticles;
    if (activeKey !== "ALL") {
      list = list.filter(x => x.cid === activeKey);
    }

    // 关键词过滤（标题/摘要/分类/标签/描述）
    if (kw) {
      list = list.filter(x => {
        const t = (x.title || "").toLowerCase();
        const s = (x.summary || "").toLowerCase();
        const c = (x.category || "").toLowerCase();
        const d = (x.desc || "").toLowerCase();
        const tags = (x.tags || []).join(" ").toLowerCase();
        return t.includes(kw) || s.includes(kw) || c.includes(kw) || d.includes(kw) || tags.includes(kw);
      });
    } else {
      // ✅ 无关键词：默认展示前 18 条
      list = list.slice(0, 18);
    }

    this.setData({ viewList: list });
  },

